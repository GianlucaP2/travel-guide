  Future<List<LatLng>> getRoute() async {
    // Try cache first
    final prefs = await SharedPreferences.getInstance();
    final cached = prefs.getString(_cacheKey);
    if (cached != null) {
      final data = jsonDecode(cached) as Map<String, dynamic>;
      final ts = data['ts'] as int;
      if (DateTime.now().millisecondsSinceEpoch - ts < _cacheTtlDays * 86400000) {
        final coords = (data['coords'] as List)
            .map((c) => LatLng((c as List)[0].toDouble(), c[1].toDouble()))
            .toList();
        return coords;
      }
    }

    // Try OSRM
    try {
      final coordStr = _osrmWaypoints.map((w) => '${w[0]},${w[1]}').join(';');
      final response = await _dio.get(
        'https://router.project-osrm.org/route/v1/driving/$coordStr?overview=full&geometries=geojson',
      );
      if (response.statusCode == 200 && response.data['code'] == 'Ok') {
        final geometry = response.data['routes'][0]['geometry']['coordinates'] as List;
        final coords = geometry
            .map((c) => LatLng((c as List)[1].toDouble(), c[0].toDouble()))
            .toList();

        // Cache
        await prefs.setString(_cacheKey, jsonEncode({
          'ts': DateTime.now().millisecondsSinceEpoch,
          'coords': coords.map((c) => [c.latitude, c.longitude]).toList(),
        }));
        return coords;
      }
    } catch (_) {}

    // Fallback
    return _fallbackRoute.map((c) => LatLng(c[0], c[1])).toList();
  }
